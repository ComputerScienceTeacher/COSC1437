#include <iostream>

using namespace std;

int main() {
    int integerLiteral = 42;  // Integral literal
    double floatingPointLiteral = 3.14;  // Floating-point literal

    cout << "Integer Literal: " << integerLiteral << endl;
    cout << "Floating-Point Literal: " << floatingPointLiteral << endl;

    return 0;
}
