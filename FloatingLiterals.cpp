#include <iostream>

using namespace std;

int main() {
    double decimalFloatingPoint = 3.14;  // Decimal floating-point literal
    double exponentialFloatingPoint = 6.02e23;  // Exponential floating-point literal

    cout << "Decimal Floating-Point Literal: " << decimalFloatingPoint << endl;
    cout << "Exponential Floating-Point Literal: " << exponentialFloatingPoint << endl;

    return 0;
}