#include <iostream>

using namespace std;

int main() {
    // Variable declaration and initialization
    int x = 42;
    double pi = 3.14;

    // Variable assignment
    x = 10;
    pi = 3.14159;

    // Printing variables
    cout << "x: " << x << endl;
    cout << "pi: " << pi << endl;

    return 0;
}
