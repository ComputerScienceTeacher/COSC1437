#include <iostream>

using namespace std;

int main() {
    int decimalLiteral = 42;  // Decimal literal
    int hexadecimalLiteral = 0xFF;  // Hexadecimal literal
    int binaryLiteral = 0b1010;  // Binary literal

    cout << "Decimal Literal: " << decimalLiteral << endl;
    cout << "Hexadecimal Literal: " << hexadecimalLiteral << endl;
    cout << "Binary Literal: " << binaryLiteral << endl;

    return 0;
}